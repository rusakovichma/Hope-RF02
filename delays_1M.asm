;-------------------пауза 100 мкс-----------------------------------
WAIT_100us:
	push	r16
	ldi		r16,$1f
W100_1:
	dec		r16
	brne	W100_1
	pop		r16
	dec		r16
	brne	WAIT_100us
	ret

;-------------------------------------------------------------------
;------------------пауза 15 мкс-------------------------------------
WAIT_15us:
	push	r16
	ldi		r16,1
W15_1:
	dec		r16
	brne	W15_1
	pop		r16
	dec		r16
	brne	WAIT_15us
	ret

;-------------------------------------------------------------------
;-------------------пауза 100 мс------------------------------------
WAIT_100ms:
	push	r16
	ldi		r16,$64
W100m_1:
;1000u
	push	r16
	ldi		r16,$f9
W100m_2:
	nop
	dec		r16
	brne	W100m_2
	pop		r16
;-----
	dec		r16
	brne	W100m_1
	pop		r16
	dec		r16
	brne	WAIT_100ms
	ret

